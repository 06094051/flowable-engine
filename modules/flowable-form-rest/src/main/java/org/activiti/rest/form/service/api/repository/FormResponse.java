package org.activiti.rest.form.service.api.repository;

import org.activiti.form.api.Form;

/**
 * @author Yvo Swillens
 */
public class FormResponse {

  protected String id;
  protected String url;
  protected String category;
  protected String name;
  protected String key;
  protected String description;
  protected int version;
  protected String resourceName;
  protected String deploymentId;
  protected String tenantId;

  public FormResponse(Form form) {
    setId(form.getId());
    setCategory(form.getCategory());
    setName(form.getName());
    setKey(form.getKey());
    setDescription(form.getDescription());
    setVersion(form.getVersion());
    setResourceName(form.getResourceName());
    setDeploymentId(form.getDeploymentId());
    setTenantId(form.getTenantId());
  }

  public String getId() {
    return id;
  }

  public void setId(String id) {
    this.id = id;
  }

  public String getUrl() {
    return url;
  }

  public void setUrl(String url) {
    this.url = url;
  }

  public String getCategory() {
    return category;
  }

  public void setCategory(String category) {
    this.category = category;
  }

  public String getName() {
    return name;
  }

  public void setName(String name) {
    this.name = name;
  }

  public String getKey() {
    return key;
  }

  public void setKey(String key) {
    this.key = key;
  }

  public String getDescription() {
    return description;
  }

  public void setDescription(String description) {
    this.description = description;
  }

  public int getVersion() {
    return version;
  }

  public void setVersion(int version) {
    this.version = version;
  }

  public String getResourceName() {
    return resourceName;
  }

  public void setResourceName(String resourceName) {
    this.resourceName = resourceName;
  }

  public String getDeploymentId() {
    return deploymentId;
  }

  public void setDeploymentId(String deploymentId) {
    this.deploymentId = deploymentId;
  }

  public String getTenantId() {
    return tenantId;
  }

  public void setTenantId(String tenantId) {
    this.tenantId = tenantId;
  }
}
